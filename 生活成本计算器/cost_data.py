# 城市 → 等级 反向映射（用于用户输入城市名后自动匹配城市等级）
# 自动从 TIER_CITIES 构建
def build_city_to_tier():
    mapping = {}
    for tier, info in TIER_CITIES.items():
        for city in info["cities"]:
            mapping[city] = tier
    return mapping

CITY_TO_TIER = build_city_to_tier()

def city_to_tier(city_name):
    """根据城市名查找所属城市等级。找不到返回 None。"""
    return CITY_TO_TIER.get(city_name)

