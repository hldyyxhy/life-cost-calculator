# 六、计算函数（供两个计算器调用）
# ============================================================

def city_factor(tier):
    """取城市成本系数"""
    return COST_FACTOR.get(tier, 1.0)


def raise_factor(level):
    """取养育档位系数"""
    return RAISE_LEVELS.get(level, {"factor": 1.0})["factor"]


def adjust_by_tier(base, tier):
    """基准值 × 城市系数"""
    return base * city_factor(tier)


def adjust_by_tier_level(base, tier, level):
    """基准值 × 城市系数 × 养育档位"""
    return base * city_factor(tier) * raise_factor(level)


def calc_personal_income_tax(taxable_monthly):
    """
    计算月度个税。
    taxable_monthly: 应纳税所得额（= 税前工资 - 五险一金 - 5000起征点 - 专项附加扣除）
    返回 (税额, 税率, 速算扣除数)
    """
    if taxable_monthly <= 0:
        return 0.0, 0.0, 0
    for upper, rate, quick in TAX_BRACKETS:
        if taxable_monthly <= upper:
            tax = taxable_monthly * rate - quick
            return max(tax, 0.0), rate, quick
    return 0.0, 0.0, 0


def fmt_money(x):
    """金额格式化：元 → 带千分号，万元则换算"""
    if x >= 10000:
        return f"{x/10000:.2f} 万元（{x:,.0f} 元）"
    return f"{x:,.0f} 元"


