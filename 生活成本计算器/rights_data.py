def get_min_wage_for_city(city_name):
    """根据城市名获取该市所在省的最低工资（第一档）"""
    prov = CITY_TO_PROVINCE.get(city_name)
    if not prov:
        return None
    return PROVINCE_MIN_WAGE.get(prov)


def estimate_unemployment_pay(province_or_city):
    """
    估算失业保险金月标准。
    返回 (元/月, 说明)
    """
    # 先查城市映射
    prov = CITY_TO_PROVINCE.get(province_or_city, province_or_city)
    if not prov:
        return None, "未找到该地区数据"
    # 精确匹配：先查省名完全匹配
    if prov in UNEMPLOYMENT_INSURANCE:
        val = UNEMPLOYMENT_INSURANCE[prov]
        if val["amount"]:
            return val["amount"], f"{prov}标准"
        elif val["ratio"]:
            mw = PROVINCE_MIN_WAGE.get(prov)
            if mw:
                return round(mw * val["ratio"]), f"按{prov}最低工资{mw}×{val['ratio']*100:.0f}%估算"
    # 模糊匹配：处理带括号的键（如"湖北（一类）"）
    best_key = None
    for key in UNEMPLOYMENT_INSURANCE:
        if key.startswith(prov + "（") or key.startswith(prov + "("):
            best_key = key
            break
    if best_key:
        val = UNEMPLOYMENT_INSURANCE[best_key]
        if val["amount"]:
            return val["amount"], f"{best_key}标准"
        elif val["ratio"]:
            mw = PROVINCE_MIN_WAGE.get(prov)
            if mw:
                return round(mw * val["ratio"]), f"按{prov}最低工资{mw}×{val['ratio']*100:.0f}%估算"
    # fallback
    fb = UNEMPLOYMENT_FALLBACK.get(prov)
    if fb:
        if fb["ratio"] is None:
            return None, fb.get("note", "需查阅当地人社局公告")
        mw = PROVINCE_MIN_WAGE.get(fb["province"])
        if mw and fb["ratio"]:
            return round(mw * fb["ratio"]), f"按{fb['province']}最低工资{mw}×{fb['ratio']*100:.0f}%估算"
    return None, "需查阅当地人社局公告"


def unemploy_duration(years_of_insurance):
    """
    根据累计缴费年限计算失业保险金最长领取月数。
    years_of_insurance: 累计缴费年数（精确到年，向上取整）
    """
    if years_of_insurance < 1:
        return 0
    for min_years, months in sorted(UNEMPLOYMENT_DURATION, reverse=True):
        if years_of_insurance >= min_years:
            return months
    return 0


def calc_injury_one_time(grade, monthly_wage):
    """
    计算一次性伤残补助金。
    grade: 1-10 级
    monthly_wage: 本人工资（受伤前12个月平均月缴费工资）
    返回 (月数, 金额)
    """
    months = INJURY_ONE_TIME.get(grade, 0)
    return months, months * monthly_wage


def calc_injury_pension(grade, monthly_wage):
    """
    计算伤残津贴月标准（1-6级）。
    返回 (比例, 月金额, 支付方)
    """
    ratio = INJURY_PENSION_RATIO.get(grade)
    if not ratio:
        return None, 0, "无（7-10级不享受按月伤残津贴）"
    payer = "工伤保险基金" if grade <= 4 else "用人单位"
    return ratio, monthly_wage * ratio, payer


def get_province_injury_extra(province, grade):
    """
    查询某省一次性工伤医疗补助金和伤残就业补助金标准。
    返回 (医疗补助金, 就业补助金, 基数说明) 或 (None, None, 说明)
    """
    data = PROVINCE_INJURY_EXTRA.get(province)
    if not data:
        return None, None, "数据未收录，请查阅当地公告"
    level_data = data["levels"].get(grade)
    if not level_data:
        return None, None, "该等级数据未收录"
    return level_data[0], level_data[1], data["base"]


